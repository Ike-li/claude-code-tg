@pytest.mark.skipif(os.name == "nt", reason="POSIX mode bits are not stable on Windows")
@pytest.mark.parametrize("force", [False, True])
def test_cmd_init_preserves_env_parent_permissions(monkeypatch, tmp_path, force):
    """Interactive init should secure .env without chmodding the project dir."""
    project_dir = tmp_path / "project"
    project_dir.mkdir()
    project_dir.chmod(0o755)
    env_file = project_dir / "bot.env"
    if force:
        env_file.write_text("ORIGINAL=1", encoding="utf-8")
        env_file.chmod(0o644)

    answers = _answers(["token", "111"])
    monkeypatch.setattr("builtins.input", lambda _prompt: next(answers))
    monkeypatch.setattr("claude_code_tg.cli_init.shutil.which", lambda _cmd: None)

    args = MagicMock()
    args.env = str(env_file)
    args.force = force
    args.full = False
    cmd_init(args)

    assert env_file.stat().st_mode & 0o777 == 0o600
    assert project_dir.stat().st_mode & 0o777 == 0o755


def test_cmd_init_rejects_existing_env_without_force(tmp_path):
    """Existing env files are not overwritten unless --force is supplied."""
    env_file = tmp_path / "bot.env"
    env_file.write_text("ORIGINAL=1", encoding="utf-8")

    args = MagicMock()
    args.env = str(env_file)
    args.force = False
    with pytest.raises(SystemExit) as exc:
        cmd_init(args)

    assert exc.value.code == 1
    assert env_file.read_text(encoding="utf-8") == "ORIGINAL=1"


