    config = env_file.read_text(encoding="utf-8")
    assert "Generated by tgcc init; keep this file 0600" in config
    assert "Telegram user IDs are comma-separated numeric IDs" in config
    assert "CLAUDE_PROJECT_DIR should point at the repo Claude may edit" in config
    assert "defaults to bypassPermissions" in config
    assert "Use default or plan before starting" in config
    assert "ATTACHMENT_MODE=path passes instance cache paths" in config
    assert "CLAUDE_SKIP_PERMISSIONS is a legacy broad bypass" in config
    assert "TELEGRAM_BOT_TOKEN=123456:ABCDEF_test_token" in config
    assert "ADMIN_USER_IDS=123456789" in config
    assert "ALLOWED_USER_IDS=987654321" in config
    assert "CLAUDE_PROJECT_DIR=/tmp/project" in config
    assert "CLAUDE_TIMEOUT=600" in config
    assert "QUEUE_MAX_SIZE=5" in config
    assert "CLAUDE_PERMISSION_MODE=acceptEdits" in config
    assert "CLAUDE_MODEL=sonnet" in config
    assert "CLAUDE_EFFORT=high" in config
    assert "CLAUDE_CLI_RESUME_COMPAT=true" in config
    assert "ATTACHMENT_MAX_MB=30" in config
    assert "ATTACHMENT_MODE=copy-to-project" in config
    assert "ATTACHMENT_RETENTION_DAYS=14" in config
    assert "CLAUDE_SKIP_PERMISSIONS=false" in config
    assert "LOG_INTERACTIONS=true" in config
    assert "CLAUDE_COMMAND_MENU=true" in config
    assert "TELEGRAM_DRAFT_PREVIEW=true" in config
    assert "TELEGRAM_MINI_APP_ENABLED=true" in config
